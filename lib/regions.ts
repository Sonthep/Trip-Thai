/** Trips that go to/from a province in this region */
export function getTripsByRegion(region: RegionData): TripDetail[] {
  const provinceSet = new Set(region.provinces)
  return TRIPS.filter((t) => {
    const norm = (s: string) => s.trim()
    return provinceSet.has(norm(t.to)) || provinceSet.has(norm(t.from))
  }).slice(0, 6)
}
